// Handle CPU exceptions
void isr_handler(registers_t regs) {
    if (regs.int_no < 32) {
        terminal_writestring("\nEXCEPTION: ");
        char s[32];
        itoa(regs.int_no, s);
        terminal_writestring(s);
        terminal_writestring(" (");
        terminal_writestring(exception_messages[regs.int_no]);
        terminal_writestring(")\n");
        
        // Print more debug info for serious errors
        if (regs.int_no == 8 || regs.int_no == 13 || regs.int_no == 14) {
            terminal_writestring("Error code: ");
            itoa(regs.err_code, s);
            terminal_writestring(s);
            terminal_writestring("\nEIP: ");
            itoa(regs.eip, s);
            terminal_writestring(s);
            terminal_writestring("\n");
        }
        
        for(;;); // Halt the system
    }
    
    // If we registered a handler, call it
    if (interrupt_handlers[regs.int_no] != 0) {
        isr_t handler = interrupt_handlers[regs.int_no];
        handler(regs);
    }
}

// Handle hardware interrupts
void irq_handler(registers_t regs) {
    // Send EOI (End of Interrupt) to the PICs
    pic_send_eoi(regs.int_no - 32);
    
    // Handle the interrupt if we have a handler
    if (interrupt_handlers[regs.int_no] != 0) {
        isr_t handler = interrupt_handlers[regs.int_no];
        handler(regs);
    }
}

// Register an interrupt handler
void register_interrupt_handler(uint8_t n, isr_t handler) {
    interrupt_handlers[n] = handler;
}

